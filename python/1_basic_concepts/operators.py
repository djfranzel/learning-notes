# standard for plus, minus, multiply
# division gets different

# will return the most precise of the ones
print(2 + 2.0)

# this is standard division ! always returns float
print(6/2)

# this is integer division
print(6//2)
print(7.0//2)
# this will always round down to the nearest whole integer

# remember that if any one of the numbers is a float, the integer divider '//' will output the most precise of the two

# modulus is as it is
print(7 % 2)

# cannot divide by zero! causes error
# print(5/0)

# power operator is **
print(2 ** 3)