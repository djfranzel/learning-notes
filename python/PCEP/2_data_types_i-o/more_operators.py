
# this shows positive/negative of numbers
print(+12)
print(-2)

# python will preserve basic order of operations
print(2 + 3 * 2)

# because of the way floats are converted to binary, they aren't exactly correct in precision 
print(0.1)
print(0.1 + 0.1 + 0.1) # this shows the non-exact addition that isn't equal to 0.3

# this is 'right sided binding', so it starts from the right
print(2 ** 2 ** 3)
